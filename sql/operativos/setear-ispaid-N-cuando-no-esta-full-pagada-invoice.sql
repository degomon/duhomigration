UPDATE C_INVOICE
SET
	ISPAID = 'N'
WHERE
	C_INVOICE_ID IN (
		WITH
			DTW AS (
				SELECT
					I.C_INVOICE_ID,
					INVOICEOPEN (I.C_INVOICE_ID, NULL)
				FROM
					C_INVOICE I
				WHERE
					I.ISPAID = 'Y'
					AND I.DATEACCT >= '20240701'
			)
		SELECT
			C_INVOICE_ID
		FROM
			DTW
		WHERE
			INVOICEOPEN > 0
	)